% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
tic;
pjg       = str2num(get(handles.edit4,'String'));
lbr       = str2num(get(handles.edit5,'String'));
a         = str2num(get(handles.edit6,'String'));
b         = str2num(get(handles.edit7,'String'));
N         = str2num(get(handles.edit8,'String'));
gen       = str2num(get(handles.edit9,'String'));
probCross = str2num(get(handles.text24,'String'));
probMut   = str2num(get(handles.text26,'String'));

P = pjg;
L = lbr;

A = P*L;

rec1 = a*a;
rec2 = a*b;

arr1 = zeros([N gen]);
arr2 = zeros([N gen]);

%GENERATE POPULATION
while true
    for i=1:N
        temp = 0;
        for j=1:gen
            arr1(i,j)=round(rand(1));
        end
        for j=1:gen
            temp = temp + arr1(i,j)*2^(gen-j);
        end
        arr1(i,gen+1) = temp;
    end

    for i=1:N
        temp = 0;
        for j=1:gen
            arr2(i,j)=round(rand(1));
        end
        for j = 1:gen
            temp = temp + arr2(i,j)*2^(gen-j);
        end
        arr2(i,gen+1) = temp;
    end
    
    if (A-arr1(:,gen+1)*rec1-arr2(:,gen+1)*rec2>=0)
        break
    end
end

A1 = arr1(:,1:gen);
A2 = arr2(:,1:gen);

kumpulanNilaiFitness = zeros([N 1000]);
nilaiMax = zeros([1 1000]);
setHasilGA_N = int16([]);
setHasilGA_M = int16([]);

for counter = 1:1000
    [hasilGAn, hasilGAm] = GenAlgCSP(A1,A2,rec1,rec2,probCross,probMut);
    setHasilGA_N{counter} = hasilGAn;
    setHasilGA_M{counter} = hasilGAm;
    kumpulanNilaiFitness(:,counter) = hasilGAn(:,gen+2);
    A1 = hasilGAn(:,1:gen);
    A2 = hasilGAm(:,1:gen);
end

%kumpulanNilaiFitness
outputf = max(kumpulanNilaiFitness(kumpulanNilaiFitness<=A));
arrIndexCol = ceil(find(kumpulanNilaiFitness==outputf)/N);
indexCol = arrIndexCol(1);
arrIndexRow = find(kumpulanNilaiFitness(:,indexCol)==outputf);
indexRow = arrIndexRow(1);

set_of_N = setHasilGA_N{indexCol};
set_of_M = setHasilGA_M{indexCol};
 
set(handles.edit1,'String',set_of_N(indexRow,gen+1));
set(handles.edit2,'String',set_of_M(indexRow,gen+1));

set(handles.edit3,'String',A-outputf);
set(handles.edit10,'String',toc);

